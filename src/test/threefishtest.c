void runtest(char *name, int size, const uint8_t *input, const uint8_t *key, const uint8_t *tweak, const uint8_t *result)
{
    uint8_t text[128];
    
    printf("%s:\n", name);
    memcpy(text, input, size);

    threefish(THREEFISH_ENCRYPT, size, key, tweak, text);
    if(memcmp(text, result, size) == 0)
        printf("\tEncryption success\n");
    else 
        printf("\tEncryption failed\n");

    memcpy(text, result, size);
    threefish(THREEFISH_DECRYPT, size, key, tweak, text);
    if(memcmp(text, input, size) == 0)
        printf("\tDecryption success\n");
    else 
        printf("\tDecryption failed\n");
}


